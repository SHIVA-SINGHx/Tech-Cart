import React, { useState } from "react";
import { AppWindowIcon, CodeIcon, Loader } from "lucide-react";
import { Button } from "@/components/ui/button";
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { useParams } from "react-router-dom";
import { useDispatch, useSelector } from "react-redux";
import { store } from "@/redux/store.js";
import userlogo from "@/assets/user.avif";
import { toast } from "sonner";
import axios from "axios";
import { setUser } from "@/redux/userSlice";

const Profile = () => {
  const { user } = useSelector((store) => store.user);
  const [loading, setLoading] = useState(false);
  const params = useParams();
  const userId = params.userId;

  const [updateUser, setUpdateUser] = useState({
    firstName: user?.firstName,
    lastName: user?.lastName,
    email: user?.email,
    phoneNo: user?.phoneNo,
    address: user?.address,
    zipCode: user?.zipCode,
    profilePic: user?.profilePic,
    city: user?.city,
    role: user?.role,
  });

  const [file, setFile] = useState(null);
  const dispatch = useDispatch();

  const handleChange = (e) => {
    setUpdateUser({ ...updateUser, [e.target.name]: e.target.value });
  };

  const handleFileChange = (e) => {
    const selectedFile = e.target.files[0];
    setFile(selectedFile);
    setUpdateUser({
      ...updateUser,
      profilePic: URL.createObjectURL(selectedFile),
    });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    const accessToken = localStorage.getItem("accessToken");

    try {
      const formData = new FormData();
      formData.append("firstName", updateUser.firstName),
        formData.append("lastName", updateUser.lastName),
        formData.append("email", updateUser.email),
        formData.append("address", updateUser.address),
        formData.append("city", updateUser.city),
        formData.append("zipCode", updateUser.zipCode),
        formData.append("phoneNo", updateUser.phoneNo),
        formData.append("role", updateUser.role);

      if (file) {
        formData.append("file", file);
      }
      const res = await axios.put(
        `https://tech-cart-xsfi.vercel.app/api/v1/user/update/${userId}`,
        formData,
        {
          headers: {
            Authorization: `Bearer ${accessToken}`,
            "Content-Type": "multipart/form-data",
          },
        }
      );
      console.log("Access Token:", accessToken);

      if (res.data.success) {
        toast.success(res.data.message);
        console.log("Updated Sucessfully");
        dispatch(setUser(res.data.user));
      }
    } catch (error) {
      console.log(error);
      toast.error("Failed to update this profile");
    }
  };

  return (
    <div className="pt-20 min-h-screen bg-gray-100 flex items-center justify-center">
      <Tabs defaultValue="profile">
        <TabsList>
          <TabsTrigger value="profile">Profile</TabsTrigger>
          <TabsTrigger value="orders">Orders</TabsTrigger>
        </TabsList>
        <TabsContent value="profile">
          <div>
            <div className="flex flex-col justify-center items-center bg-gray-100">
              <h1 className="font-bold mb-7 text-2xl text-gray-800">
                Update Profile
              </h1>
              <div className="w-full flex gap-10 justify-between items-start px-7 max-w-2xl">
                {/* profile image */}
                <div className="flex flex-col items-center">
                  <img
                    src={updateUser?.profilePic || userlogo}
                    alt="profile"
                    className="w-32 h-32 rounded-full object-cover border-4 border-blue-500"
                  />
                  <Label className="mt-4 cursor-pointer bg-pink-500 text-white px-4 py-2 rounded-lg hover:bg-pink-700 ">
                    {" "}
                    Change Picture
                    <input
                      type="file"
                      accept="image/*"
                      className="hidden"
                      onChange={handleFileChange}
                    />
                  </Label>
                </div>
                {/* profile form */}
                <form
                  className="space-y-4 shadow-lg p-5 rounded-lg bg-white"
                  onSubmit={handleSubmit}
                >
                  <div className="grid grid-cols-2 gap-4">
                    <div>
                      <Label className="block text-sm font-medium">
                        First Name
                      </Label>
                      <input
                        type="text"
                        name="firstName"
                        placeholder="John"
                        value={updateUser.firstName}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1"
                      />
                    </div>
                    <div>
                      <Label className="block text-sm font-medium">
                        Last Name
                      </Label>
                      <input
                        type="text"
                        name="lastName"
                        placeholder="Doe"
                        value={updateUser.lastName}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1"
                      />
                    </div>
                    <div>
                      <Label className="block text-sm font-medium">Email</Label>
                      <input
                        type="email"
                        name="email"
                        placeholder="john@gmail.com"
                        value={updateUser.email}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1 bg-gray-100 cursor-not-allowed"
                      />
                    </div>
                    <div>
                      <Label className="block text-sm font-medium">
                        Phone Number
                      </Label>
                      <input
                        type="text"
                        name="phoneNo"
                        placeholder="+91"
                        value={updateUser.phoneNo}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1 bg-gray-100 cursor-not-allowed"
                      />
                    </div>

                    <div>
                      <Label className="block text-sm font-medium">
                        Address
                      </Label>
                      <input
                        type="text"
                        name="address"
                        placeholder="Enter your Address"
                        value={updateUser.address}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1"
                      />
                    </div>
                    <div>
                      <Label className="block text-sm font-medium">City</Label>
                      <input
                        type="text"
                        name="city"
                        placeholder="Enter your City"
                        value={updateUser.city}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1"
                      />
                    </div>
                    <div>
                      <Label className="block text-sm font-medium">
                        Zip Code
                      </Label>
                      <input
                        type="text"
                        name="zipCode"
                        placeholder="Enter your ZipCode"
                        value={updateUser.zipCode}
                        onChange={handleChange}
                        className="w-full border rounded-lg px-3 py-2 mt-1"
                      />
                    </div>
                  </div>
                  <Button
                    type="submit"
                    className="w-full mt-4 bg-pink-500 hover:bg-pink-700 text-white font-semibold py-2 rounded-lg flex justify-center items-center gap-2"
                    disabled={loading}
                  >
                    {loading ? (
                      <>
                        <Loader /> Please wait...
                      </>
                    ) : (
                      "Update Profile"
                    )}
                  </Button>
                </form>
              </div>
            </div>
          </div>
        </TabsContent>
        <TabsContent value="orders">
          <Card>
            <CardHeader>
              <CardTitle>Password</CardTitle>
              <CardDescription>
                Change your password here. After saving, you&apos;ll be logged
                out.
              </CardDescription>
            </CardHeader>
            <CardContent className="grid gap-6">
              <div className="grid gap-3">
                <Label htmlFor="tabs-demo-current">Current password</Label>
                <Input id="tabs-demo-current" type="password" />
              </div>
              <div className="grid gap-3">
                <Label htmlFor="tabs-demo-new">New password</Label>
                <Input id="tabs-demo-new" type="password" />
              </div>
            </CardContent>
            <CardFooter>
              <Button>Save password</Button>
            </CardFooter>
          </Card>
        </TabsContent>
      </Tabs>
    </div>
  );
};

export default Profile;
